package OBJETOS;


public class VEICULO_ {
    
    private String Marca;
    private String Modelo;
    private float Ano_Fabricacao;
    private float Placa;
    private String Cor;
    private String Tipo_Veiculo;
    private String Num_Chassi;

    public String getMarca() {
        return Marca;
    }

    public void setMarca(String Marca) {
        this.Marca = Marca;
    }

    public String getModelo() {
        return Modelo;
    }

    public void setModelo(String Modelo) {
        this.Modelo = Modelo;
    }

    public float getAno_Fabricacao() {
        return Ano_Fabricacao;
    }

    public void setAno_Fabricacao(float Ano_Fabricacao) {
        this.Ano_Fabricacao = Ano_Fabricacao;
    }

    public float getPlaca() {
        return Placa;
    }

    public void setPlaca(float Placa) {
        this.Placa = Placa;
    }

    public String getCor() {
        return Cor;
    }

    public void setCor(String Cor) {
        this.Cor = Cor;
    }

    public String getTipo_Veiculo() {
        return Tipo_Veiculo;
    }

    public void setTipo_Veiculo(String Tipo_Veiculo) {
        this.Tipo_Veiculo = Tipo_Veiculo;
    }

    public String getNum_Chassi() {
        return Num_Chassi;
    }

    public void setNum_Chassi(String Num_Chassi) {
        this.Num_Chassi = Num_Chassi;
    }
    
    
}
